const DEFAULT_TIMESTAMP = "1970-01-01T00:00:00.000Z";

function normalizeAnimation(a: any): Animation3D {
  const slug = String(a.slug || a.id || "").replace(/^\//, "");
  const price = Number(a.price ?? 0);
  return {
    id: String(a.id || slug),
    slug,
    title: String(a.title || slug),
    description: a.description ? String(a.description) : undefined,
    shortDescription: a.shortDescription ? String(a.shortDescription) : undefined,
    renderer: a.renderer || "custom",
    previewType: a.previewType || "iframe",
    previewSrc: a.previewSrc || undefined,
    htmlFile: a.htmlFile || undefined,
    animationTypeId: a.animationTypeId || undefined,
    tags: Array.isArray(a.tags) ? a.tags : [],
    performanceTier: a.performanceTier || undefined,
    colorPalette: Array.isArray(a.colorPalette) ? a.colorPalette : [],
    compatibleBackgrounds: Array.isArray(a.compatibleBackgrounds) ? a.compatibleBackgrounds : [],
    dependencies: Array.isArray(a.dependencies) ? a.dependencies : [],
    paramsSchema: Array.isArray(a.paramsSchema) ? a.paramsSchema : [],
    durationMs: typeof a.durationMs === "number" ? a.durationMs : undefined,
    price,
    isFree: typeof a.isFree === "boolean" ? a.isFree : price <= 0,
    features: Array.isArray(a.features) ? a.features : [],
    previewImageUrl: a.previewImageUrl || undefined,
    previewVideoUrl: a.previewVideoUrl || undefined,
    screenshots: Array.isArray(a.screenshots) ? a.screenshots : [],
    isPublished: typeof a.isPublished === "boolean" ? a.isPublished : true,
    isFeatured: typeof a.isFeatured === "boolean" ? a.isFeatured : false,
    sortOrder: typeof a.sortOrder === "number" ? a.sortOrder : 0,
    createdAt: a.createdAt || DEFAULT_TIMESTAMP,
    updatedAt: a.updatedAt || DEFAULT_TIMESTAMP,
  };
}

export const animations3D: Animation3D[] = rawAnimations.map(normalizeAnimation);
